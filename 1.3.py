
kriszti_ar = 150  # Kriszti egy csokoládészelet ára
kriszti_db = 2    # Kriszti által vett darabszám
tomi_ar = 20      # Tomi egy cukorka ára
tomi_db = 5       # Tomi által vett darabszám
cili_ar = 50      # Cili egy keksz ára
cili_db = 3       # Cili által vett darabszám

# Fizetendő összegek kiszámítása
kriszti_fizet = kriszti_ar * kriszti_db
tomi_fizet = tomi_ar * tomi_db
cili_fizet = cili_ar * cili_db

# Eredmények kiírása
print(f"Kriszti fizet: {kriszti_fizet} Ft-ot")
print(f"Tomi fizet: {tomi_fizet} Ft-ot")
print(f"Cili fizet: {cili_fizet} Ft-ot")